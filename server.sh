#!/bin/bash
#-------------VARS--------------------------------------------------
WORKERS=$1              # Кол-во параллельных процессов
INT=192.168.10.151      # IP интерфейса тестирования
#-------------------------------------------------------------------
for ((p=1; p<=$WORKERS; p++))
do
PORT=$((13000+$p))
#CORE=$((2*$p))
iperf3 -s -B $INT -p $PORT -i 0 -1 -V &
#iperf  -s -B 192.168.10.151 -p $PORT -e -l 8192 -u &
done
